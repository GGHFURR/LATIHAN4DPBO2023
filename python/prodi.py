class prodi:

    # constructor prodi
    def __init__(self, nama_prodi, kode, fakultas):
        self.nama_prodi = nama_prodi
        self.kode = kode
        self.fakultas = fakultas

     # getter/setter methods untuk class prodi
    def get_nama_prodi(self):
        return self.nama_prodi

    def get_kode(self):
        return self.kode

    def get_fakultas(self):
        return self.fakultas
